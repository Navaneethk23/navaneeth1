void main() {
  var mark = 95;
  if (mark >= 90){
    print("excellent");
  }
  else if (mark >= 80){
    print("great");
  }
  else if (mark >= 70){
    print("good");
  }
  else if (mark >= 60){
    print("average");
  }
  else if (mark >= 50){
    print("pass");
  }
  else if (mark > 40){
    print("fail");
  }

}
