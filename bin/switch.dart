void main(){
  String weeks="mon";
  switch(weeks){
    case"sun":
      print("sunday");
      break;
    case"mon":
      print("monday");
      break;
    case"tue":
      print("tuesday");
      break;
    case"wed":
      print("wednesday");
      break;
    case "thur":
      print("thursday");
      break;
    case"fri":
      print("friday");
      break;
    default:
      print("saturday");
      break;

  }

}